var c = 1

console.log('Vai começar...')

while (c <= 10) { // Laço de repetição While
    console.log(`Passo ${c} com o While`)
    c++
}

console.log('FIM!')

console.log('--------------------------------------------')

console.log('Vai começar...')

for (var c = 1; c <= 10; c++) { // Laço de repetição For
    console.log(`Passo ${c} com o For`)
}

console.log('FIM!')